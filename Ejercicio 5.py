#operadores de comparacion

x = 10
y = 5

# Sumar " + "
suma=x+y
print("Este es el resultado de la suma de x + y =",suma )

# Resta " - "
resta = x-y
print("Este es el resultado de la resta de x - y =",resta )

# Multiplicacion " * "
multiplicacion=x*y
print("Este es el resultado de la multiplicacion de x * y =",multiplicacion )

# División " / "
division = x/y
print("Este es el resultado de la division de x / y ", division)

# Producto entero de una  división " // "
valorEntero=x//y
print("Este es el producto entero de la divicion de x // y =",valorEntero )

# Exponente " ** "
elevado=x**y
print("Este es el exponente de x ** y =",elevado )

# OPERADORES COMPARATIVOS 
'''
Al hacer un valor conparativo con estos datos 
Python te responde un valor de tipo Boole
usando True o False segun corresponda
los comparadores hace la comprobacion de ambos lados haciendo comparativas entre los
datos de la variable
'''
# Mayor que " > "
mayor=x>y
print("x es mayor que y:", mayor)

# Menor que " < "
menor=x<y
print("x es menor que y:", menor)

# Igual a " == "
igual=x==y
print("x y y son iguales:",igual )

# No igual a " != "
distinto=x!=y
print("x no es igual a y:", distinto )

# Mayor o igual " >= "
alMenos =x>=y
print("x es mayor o igual a y:", alMenos)

# Menor o igual " <= "
aLoMucho=x<=y
print("x es menor o igual a y:", aLoMucho)
 